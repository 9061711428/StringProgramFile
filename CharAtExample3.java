package stringmethods;

public class CharAtExample3 {
	public static void main(String args[])
	{
	String str="welcome to FACE portal";
	int strLength=str.length();
	System.out.println("character is 1st index is :"+str.charAt(1));//Fetching the 1st character
	System.out.println("character at last index is:"+str.charAt(strLength-1));//fetching the last character of  the string.
	}

}
